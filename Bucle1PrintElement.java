public class Bucle1PrintElement {
    public static void main(String[] args) {
        int[] numbers = { 1, 2, 3, 4, 5, 6 };

        for (int i = 0; i < numbers.length; i++) {
            System.out.println("\n" + "------------------" + "\n" + numbers[i] + "\n" + "------------------");
        }
    }
}