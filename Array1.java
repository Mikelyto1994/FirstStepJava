public class Array1 {
    public static void main(String[] args) {
        // Crear una variable de tipo array que contenga tres elementos de tipo entero
        int[] numeros = { 10, 20, 30 };

        // Sumar los tres elementos de forma manual
        int suma = numeros[0] + numeros[1] + numeros[2];

        // Imprimir por consola el resultado
        System.out.println("La suma de los elementos es: " + suma);
    }
}
